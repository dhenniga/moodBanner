package com.moodbanner.dev.any.Fonts;


public class ValueFont {

    private int id;
    private String name, fontThumb, fontFile;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFontThumb() {
        return fontThumb;
    }

    public void setFontThumb(String fontThumb) {
        this.fontThumb = fontThumb;
    }

    public String getFontFile() {
        return fontFile;
    }

    public void setFontFile(String fontFile) {
        this.fontFile = fontFile;
    }
}
